    public void setRecordsToTable(){
        try{
            Class.forName("org.apache.derby.jdbc.EmbeddedDriver");
            Connection con = DriverManager.getConnection("jdbc:derby://localhost:1527/fees_ms","root","root");
            PreparedStatement pst = con.prepareStatement("select * from fees_details");
            ResultSet rs = pst.executeQuery();
            
            while(rs.next()){
                
                String recieptNo = rs.getString("reciept_no");
                String rollNo = rs.getString("roll_no");
                String studentName = rs.getString("student_name");
                String courseName = rs.getString("course_name");
                String amount = rs.getString("amount");
                String totalAmount = rs.getString("total_amount");
                String date = rs.getString("date_of_receipt");
                String paymentMode = rs.getString("payment_mode");
                String remark = rs.getString("remark");
                
                Object[] obj = {recieptNo,rollNo,studentName,courseName,amount,totalAmount,date,paymentMode,remark};
                model = (DefaultTableModel)tbl_feeData.getModel();
                model.addRow(obj);
            }
        }catch(Exception e){
            e.printStackTrace();
        }
    }
    
    public void search(String str){
        model = (DefaultTableModel)tbl_feeData.getModel();
        TableRowSorter<DefaultTableModel> trs = new TableRowSorter<>(model);
        tbl_feeData.setRowSorter(trs);
        trs.setRowFilter(RowFilter.regexFilter(str));
    }
    
    public void clearTable(){
        DefaultTableModel model = (DefaultTableModel)tbl_feeData.getModel();
        model.setRowCount(0);
    }

